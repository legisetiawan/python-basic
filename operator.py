# Operator matematika
x = 2
y = 10
print(f"hasil bagi = {y} / {x}" )
bagi = int(y/x) # conversi bilangan float ke int
print(bagi)
print(f"hasil kali = {y} * {x}" )
kali = int(y*x) # conversi bilangan float ke int
print(kali)
print(f"hasil tambah = {y} + {x}" )
kali = int(y+x) # conversi bilangan float ke int
print(kali)
print(f"hasil kurang = {y} - {x}" )
kali = int(y-x) # conversi bilangan float ke int
print(kali)
print(f"hasil modulus = {y} % {x}" )
kali = int(y%x) # conversi bilangan float ke int
print(kali)


