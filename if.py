# Pernyataan if kembalianya boolean true, sering di gunakan dengan operator, logika, matematika dll
b = 70
nilai = 70
if nilai < b:
    print("tidak lulus")
if nilai > b:
    print("Besar dari 70:")
    print("lulus")
if nilai <= b:
    print("kecil sama dari 70:")
    print(" lulus")
if nilai >= b:
    print("Besar sama dari 70:")
    print(" lulus")
if nilai == b:
    print("sama dengan 70:")
    print(" lulus")
if nilai != b:
    print("Tidak sama dari 70:")
    print(" lulus")
